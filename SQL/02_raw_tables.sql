-- Patients Raw Table

CREATE OR REPLACE TABLE RAW.PATIENTS_RAW (
PATIENT_ID NUMBER,
PATIENT_NAME VARCHAR,
GENDER VARCHAR,
AGE NUMBER
);

-- Doctors Raw Table

CREATE OR REPLACE TABLE RAW.DOCTORS_RAW (
DOCTOR_ID NUMBER,
DOCTOR_NAME VARCHAR,
DEPARTMENT VARCHAR,
EXPERIENCE_YEARS NUMBER
);

