CREATE OR REPLACE TABLE RAW.APPOINTMENTS_RAW (
APPOINTMENT_ID NUMBER,
PATIENT_ID NUMBER,
DOCTOR_ID NUMBER,
APPOINTMENT_DATE DATE,
APPOINTMENT_STATUS VARCHAR,
BILLING_AMOUNT NUMBER,
TREATMENT_COST NUMBER
);

